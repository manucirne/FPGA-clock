inicio:
addi $1, %zero, %t0
addi $2, %zero, %t1
addi $3, %zero, %t2
addi $4, %zero, %t3
wea  $0(%6), %t0
wea  $1(%6), %t1
wea  $2(%6), %t2
wea  $2(%6), %t3
wea  $0(%6), %t3
jmp inicio
